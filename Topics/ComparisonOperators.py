## comparision operators 
## > >= < <= == !=

print(10 < 20)  #True
print(10 <= 20)  #True
print(10 > 20)  #False
print(10 >= 10) #True
print(10 == 10) #True
print(10 != 10) #False

## if different type of values
print(10 == "10") #False
print("10" == "10") #True


## Comparison with charcters
# in programing or machine storage, every character has numerical number 
# ord() - give integer of character

print(ord("a")) #97
print(ord("b")) #98
print(ord("B")) #66
print(ord("&")) #38

print("bat" > "apple") #True 
print("b" > "a") #True

print("b" == "B") #False



